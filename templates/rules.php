        <h3>Das Spiel</h3>
        <p> Mord In Palermo ist ein Gesellschaftsspiel.
          Zu Beginn des Spiels wird jeder Spieler seiner geheimen Rolle zugewiesen.
          Er spielt nun entweder auf der Seite der Bürger oder der Mörder.
          Die Mörder wollen unentdeckt bleiben und geben sich als Bürger aus.
          In jeder Runde bestimmen sie dabei einen Bürger, den sie "ermorden".
          Ziel der Seite der Bürger ist es die Mörder unter sich ausfindig zu machen und zu
          entlarven während die Mörder versuchen die Bürger auf eine falsche Fährte zu führen.
          In jeder Runde stimmen die Spieler dann darüber ab wer als Mörder
          infrage kommt und bestimmen einen Spieler, der "erhängt" werden soll.
          <br>
          Unter den Bürgern gibt es solche mit besonderen Fähigkeiten, wie den Arzt
          oder den Spion. Diese Fähigkeiten können dazu genutzt werden die Mörder
          ausfindig zu machen oder ihren "Mordanschlag" abzuwehren.
          Weil diese Fähigkeiten den Mördern gefährlich werden können, müssen diese
          Spieler sehr vorsichtig mit ihrer wahren Identität umgehen.
          <br>
          Ziel des Spiels ist die jeweils andere Seite zu dezimieren.
          Die Seite der Bürger nutzt dafür die rundenbasierten Abstimmungen und
          die Mörder wählen zusätzlich einen Spieler, der "ermordet" werden soll.
          Die Bürger gewinnen das Spiel, wenn alle Mörder erhängt worden sind
          und die Mörder gewinnen das Spiel, sobald weniger Bürger als Mörder noch leben.
        </p>

        <h3>Die Rollen</h3>

        <h5><i class="fas fa-fw fa-eye"></i> Spion</h5>
        <p> Als Spion ist man der Sherlock Holmes unter den Bürgern.
          Er hat die besondere Fähigkeit jede Runde die wahre Identität eines Spielers
          zu erfahren. Die Mörder haben es als Erstes auf ihn abgesehen, denn er
          kann sie am ehesten entlarven.
        </p>

        <h5><i class="fas fa-fw fa-plus"></i> Arzt</h5>
        <p> Der Arzt hat die Fähigkeit einen "Mordanschlag" abzuwehren.
          Dazu wählt er jede Runde einen Mitspieler aus und macht ihn somit Immun
          gegen den Versuch der Mörder diesen umzubringen. Dabei muss der Arzt seinen
          Instinkt nutzen denn wissen kann er nicht wen die Mörder auswählen.
          Seine Hauptaufgabe ist den Spion am Leben zu halten, damit dieser
          die Mörder entlarven kann.
        </p>

        <h5><i class="fas fa-fw fa-skull"></i> Mörder</h5>
        <p> Die Mörder müssen zusammenhalten. Sie kennen die Identität der anderen
          Mörder und können sich im Geheimen absprechen. Sie müssen sich nach außen
          als Bürger ausgeben, um am Ende der Runde nicht gehängt zu werden.
          Ihr Ziel ist es die Bürger zu töten bevor sie selber entlarvt und gehängt
          werden. Ihre ersten Zielpersonen sollten der Spion und die Ärzte sein, denn
          mit ihren besonderen Fähigkeiten können sie den Mördern gefährlich werden.
          Achtung! Die Mörder müssen sich darüber einig sein, wen sie "umbringen",
          denn sie können nur einen Mitspieler pro Runde "ermorden".
        </p>

        <h5><i class="fas fa-fw fa-user"></i> Bürger</h5>
        <p> Die "normalen" Bürger haben keine besondere Fähigkeit.
          Sie müssen auf ihren Instinkt vertrauen und aktiv in den Rundendiskussionen
          zwischen Freund oder Feind unterscheiden lernen.
          Sie stimmen in jeder Runde für einen Spieler ab, den sie für einen Mörder
          halten. "Gehängt" wird schließlich der Spieler mit den meisten Stimmen.
        </p>

        <h3>Dashboard</h3>
        <p> Das Dashboard ist die Hauptseite vom Spiel. Nachdem das Spiel gestartet ist,
          erfährt man unter "Rollen" die eigene Rolle und schaltet damit die besondere
          Fähigkeit dieser Rolle frei. Nach zwei Sekunden werden Rolle und besondere
          Fähigkeit wieder versteckt, damit niemand versehentlich auf den Bildschirm
          schielt und sofort die Identität erkennt.
          <br>
          Die Spielerliste ist das Bedienfeld für die rundenbasierten Abstimmungen.
          Auf dem Fadenkreuz lässt sich abstimmen, wer am Ende der Runde "gehängt"
          werden soll. Außerdem können Mörder, Spion und Arzt nach Betätigung des "Rollen"-Buttons
          hier ihre besondere Fähigkeit anwenden. Dazu muss das Rollenspezifische Symbol
          in der Spielerliste angewählt werden. Für jede Abstimmung kriegt man in Echtzeit
          angezeigt wie viele für einen Spieler abgestimmt haben.
        </p>

        <h3>Messenger</h3>
        <p> Auch wenn Mord in Palermo ein Face-To-Face Gesellschaftsspiel ist und eigentlich
          keine Nachrichtenfunktion benötigt, eröffnet der Messenger eine weitere Kommunikationsebene
          im Spiel. Über den "Message"-Button in der Spielerliste kann man einen privaten Chat mit dem
          ausgewähltem Mitspieler erstellen. Über den "Message"-Button oben rechts im Dashboard gelangt
          man zur Chatübersicht. Hier findet man voreingestellt einen allgemeinen Gruppenchat, der
          jeden Mitspieler beinhaltet. Somit ist das Spiel nicht mehr nur noch auf die
          sich im selben Raum befindlichen Personen begrenzt, sondern kann auch über größere
          Distanzen hinweg gespielt werden. Darüber hinaus hat man die Möglichkeit einen
          benutzerdefinierten Chat mit beliebigen Mitspielern zu erstellen.
          Für die Mörder ist diese Funktion besonders nützlich, denn sie können sich
          unbemerkt absprechen.
        </p>

